# This program demonstrates a variable
room = 503
print ("I am staying in room number")
print (room)

# variable follows the same line
print ("I am staying in room number", room)

# sep function decides the space between commas
print ("I am staying in room number", room, sep="")

# concatenate variables and str to make variable string
print ("I am staying in room number " + str(room))

#end function defines the end of print function
print("I am staying in room number", end="\t\t")
